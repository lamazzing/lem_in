#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*s1;

	s1 = (char*)s;
	i = 0;
	while (s[i] != '\0')
	{
		i++;
		if (s[i] == '\0' && c == '\0')
			return (s1 + i);
	}
	while (s[i] != s[0])
	{
		i--;
		if (s[i] == c)
			return (s1 + i);
	}
	return (NULL);
}
