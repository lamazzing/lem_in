#include "libft.h"

void	ft_lstadd(t_list **alst, t_list *new)
{
	t_list *tmp;

	tmp = *alst;
	if (tmp)
	{
		new->next = *alst;
		*alst = new;
	}
	else
		*alst = new;
}
