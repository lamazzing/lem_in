#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char *)b;
	while (len)
	{
		str[i] = (unsigned char)c;
		i++;
		len--;
	}
	return (b);
}
