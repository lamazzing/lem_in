#include "libft.h"

int	ft_isdigits(char *s)
{
	int	i;
	int	er;

	i = 0;
	er = 0;
	while (s[i] != '\0')
	{
		if (i == 0 && s[i] == '-')
			i++;
		if (!(s[i] >= '0' && s[i] <= '9'))
			er++;
		i++;
	}
	return ((er == 0) ? 1 : 0);
}
